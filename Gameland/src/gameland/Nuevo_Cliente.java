    public void enable ()
    {
        if(!nom.getText().isEmpty()&&!nit.getText().isEmpty()
                &&!telefono.getText().isEmpty()&&!direc.getText().isEmpty())
        {  aceptar.setEnabled(true);
        } else
        {  aceptar.setEnabled(false);
        }
    }
