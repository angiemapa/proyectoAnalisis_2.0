    private void nomKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nomKeyTyped
         char valido=evt.getKeyChar();
        if(Character.isDigit(valido)){
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(rootPane,"Ingrese el nombre en letras");
        }
    }//GEN-LAST:event_nomKeyTyped

    private void nitKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nitKeyTyped
         char valido=evt.getKeyChar();
        if(Character.isLetter(valido)){
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(rootPane,"Ingrese el numero");
        }
    }//GEN-LAST:event_nitKeyTyped

